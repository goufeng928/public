# 6. 练习1

arr=[[1,2,3],
     [4,5,6]]

# 6.1 将二维列表 arr 输出为如下效果。
   
# 1 4 
# 2 5 
# 3 6
     
# 1的下标：[0][0]   4的下标：[1][0]
# 2的下标：[0][1]   5的下标：[1][1]
# 3的下标：[0][2]   5的下标：[1][2]

# 6.1.1 解法1
for j in range(3):
    for i in range(2):
        print(arr[i][j], end=' ')
    print()
   
# 6.1.2 解法2
for i in range(3):
   for j in range(2):
       print(arr[j][i], end=' ')
   print()

# 6.2 将二维列表 arr 输出为如下效果。

# 4 1 
# 5 2 
# 6 3

# 6.2.1 解法1
for j in range(3):
    for i in range(1,-1,-1):
        print(arr[i][j], end=' ')
    print()

# 6.2.2 解法2
for i in range(3):
   for j in range(1,-1,-1):
       print(arr[j][i], end=' ')
   print()

